void main() {
  // Strings
  print("I am printing a string!");
  print('I am printing another string!');

  // int
  print(10);
  print(1000);

  // double
  print(10.30);
  print(1.0);

  // bool
  print(true);
  print(false);

  // List
  print([
    "Here is a string",
    10,
    10.0,
    true,
  ]);

  // List Index
  print(["Here is a string", 10, 10.0, true][0]);
  print(["Here is a string", 10, 10.0, true][2]);
}
